use gdp_vs_population;

select DISTINCT b.CBSA, a.CTYNAME, a.STATE, a.STNAME, b.`COUNTY AND STATE`, a.ESTIMATEBASE2000, a.POPESTIMATE2000,
a.POPESTIMATE2000, a.POPESTIMATE2001, a.POPESTIMATE2002, a.POPESTIMATE2003, a.POPESTIMATE2004, a.POPESTIMATE2005, 
a.POPESTIMATE2006, a.POPESTIMATE2007, a.POPESTIMATE2008, a.POPESTIMATE2009, a.POPESTIMATE2010, a.CENSUS2010POP,
b.POPESTIMATE2011, b.POPESTIMATE2012,  b.POPESTIMATE2013,  b.POPESTIMATE2014,  b.POPESTIMATE2015,  
b.POPESTIMATE2016,  b.POPESTIMATE2017,  b.POPESTIMATE2018,  b.POPESTIMATE2019,  b.POPESTIMATE2020    
from county_population_2000_2010 as a
right join county_population_2010_2020 as b 
on a.ctyname = b.ctyname
AND b.state = a.state
order by b.cbsa DESC;